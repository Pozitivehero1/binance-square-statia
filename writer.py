import requests
import os

MISTRAL_API = os.getenv("MISTRAL_API")

def write_post(data):
    prompt = f"""
Ты — профессиональный копирайтер и аналитик для платформы Binance Square.
Твоя задача — написать аналитическую статью о монете {data['basic']}.

Данные для анализа:
- Монета: {data['basic']} (тикер пары: {data['symbol']})
- Цена: {data['price']}
- Изменение за последние 10 свечей: {round(data['change'], 2)}%
- RSI: {round(data['rsi'], 2)}
- EMA20: {round(data['ema20'], 2)}
- EMA50: {round(data['ema50'], 2)}

СТИЛЬ И ТОН:
- Дружелюбный и понятный, говори о графиках простыми словами, как рассказывать историю.
- Используй простые аналогии («пульс рынка» для RSI, «пол» для поддержки, «стена сопротивления»).
- Избегай неуверенных формулировок («возможно», «может быть»), но не давай финансовых советов.
- Используй смайлы, где уместно.

СТРУКТУРА СТАТЬИ:
1. Заголовок: Цепляющий, тезисный, отражает суть анализа. Содержит тикер (${data['basic']}).
2. Вступление: Контекст и главный тезис (что сейчас происходит на графике?).
3. Основной анализ: Объясни логику происходящего простым языком. Фокусируйся на «почему» (почему цена остановилась здесь? почему этот уровень важен?). Ссылайся на ключевые технические элементы (уровни поддержки/сопротивления, EMA, RSI), объясняя их значение.
4. Практический блок: Вход, Цели (TP1, TP2, TP3), Стоп-лосс (SL) с кратким пояснением логики уровней.
5. Заключение: Краткий итог или философское наблюдение о ситуации.
6. Вопрос для аудитории: Открытый вопрос, провоцирующий обсуждение.

ТРЕБОВАНИЯ:
- Все упоминания монеты делай строго в формате ${data['basic']} (например, $BTC, $ETH). Максимум 3 упоминания
- Не используй полное имя с USDT.
- Не добавляй хештеги (#).
- Не используй звёздочки, жирный шрифт, курсив.
- Стиль живого автора, не робота.
- Не пиши "не является финансовой рекомендацией".
- Тикер упоминай максимум 3 раза, избегай спама.

Верни готовую статью в виде:
ЗАГОЛОВОК: [твой заголовок]
ТЕКСТ: [текст статьи]
"""

    r = requests.post(
        "https://api.mistral.ai/v1/chat/completions",
        headers={
            "Authorization": f"Bearer {MISTRAL_API}",
            "Content-Type": "application/json"
        },
        json={
            "model": "mistral-small",
            "messages": [{"role": "user", "content": prompt}]
        },
        timeout=60
    )

    response = r.json()
    content = response["choices"][0]["message"]["content"]
    
    # Парсим заголовок и текст
    title = ""
    text = content
    if "ЗАГОЛОВОК:" in content:
        parts = content.split("ЗАГОЛОВОК:", 1)
        if len(parts) > 1:
            title_part = parts[1].split("ТЕКСТ:", 1)
            if len(title_part) > 1:
                title = title_part[0].strip()
                text = title_part[1].strip()
            else:
                title = title_part[0].strip()
    
    # Очистка от случайного форматирования
    for ch in ['*', '_', '`', '#']:
        text = text.replace(ch, '')
        title = title.replace(ch, '')

    return title, text
